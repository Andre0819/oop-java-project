/**
 * Apple class represents an apple seed that extends the abstract class Seed.
*/
public class Apple extends Seed {

    /**
     * Creates an Apple object with predefined attributes that is passed to the
     * constructor of the abstract class Seed.
     */
    public Apple() {
        super("Apple", "Fruit tree", 10, 7, 7, 5,
                5, 10, 15, 200,5, 25);
    }
}
