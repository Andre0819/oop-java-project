/**
 * Turnip class represents a turnip seed that extends the seed class.
 */
public class Turnip extends Seed {

    /**
     * Creates a Turnip object with predefined attributes that is passed to the
     * constructor of the abstract class Seed.
     */
    public Turnip() {
        super("Turnip", "Root crop", 2, 1, 2,
                0, 1, 1, 2, 5, 6, 5);
    }
}
