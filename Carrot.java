/**
 * Carrot class represents a carrot seed that extends the abstract class Seed.
 */
public class Carrot extends Seed {

    /**
     * Creates a Carrot object with predefined attributes that is passed to the
     * constructor of the abstract class Seed.
     */
    public Carrot() {
        super("Carrot", "Root crop", 3, 1, 2, 0,
                1, 1, 2, 10, 9, 7.5);
    }
}
