/**
 * DefaultFarmer class represents a default farmer type that extends the FarmerType class.
 */
public class DefaultFarmer extends FarmerType {

    /**
     * Creates a DefaultFarmer object with predefined attributes that is passed to the
     * constructor of the abstract class FarmerType.
     */
    public DefaultFarmer() {
        super("Default Farmer", 0, 0, 0,
                0, 0, 0);
    }
}
