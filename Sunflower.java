/**
 * Sunflower class represents a sunflower seed that extends the seed class.
 */
public class Sunflower extends Seed {

    /**
     * Creates a Sunflower object with predefined attributes that is passed to the
     * constructor of the abstract class Seed.
     */
    public Sunflower() {
        super("Sunflower", "Flower" , 3, 2, 3, 1,
                2, 1, 1, 20,19, 7.5);
    }
}
