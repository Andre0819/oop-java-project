/**
 * Mango class represents a mango seed that extends the abstract class Seed.
 */
public class Mango extends Seed {

    /**
     * Creates a Mango object with predefined attributes that is passed to the
     * constructor of the abstract class Seed.
     */
    public Mango() {
        super("Mango", "Fruit tree", 10, 7, 7, 4,
                4, 5, 15, 100, 8, 25);
    }
}