/**
 * Tulips class represents a tulips seed that extends the seed class.
 */
public class Tulips extends Seed {

    /**
     * Creates a Tulips object with predefined attributes that is passed to the
     * constructor of the abstract class Seed.
     */
    public Tulips() {
        super("Tulips", "Flower", 2, 2, 3, 0,
                1, 1, 1, 10, 9, 5);
    }
}
