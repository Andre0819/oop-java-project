/**
 * Rose class represents a rose seed that extends the seed class.
 */
public class Rose extends Seed {

    /**
     * Creates a Rose object with predefined attributes that is passed to the
     * constructor of the abstract class Seed.
     */
    public Rose() {
        super("Rose", "Flower", 1, 1, 2, 0,
                1, 1, 1, 5, 5, 2.5);
    }
}
