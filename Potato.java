/**
 * Potato class represents a potato seed that extends the abstract class Seed.
 */
public class Potato extends Seed {

    /**
     * Creates a Potato object with predefined attributes that is passed to the
     * constructor of the abstract class Seed.
     */
    public Potato() {
        super("Potato", "Root Crop", 5, 3, 4, 1,
                2, 1, 10, 20, 3, 12.5);
    }
}
